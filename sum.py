def sum(a,b):
    return a+b

def diff(a,b):
    return a-b

def mul(a,b):
    return a*b

def div(a,b):
    return a/b

def mod(a,b):
    return a%b

a = int(input("enter the value of a: "))
b = int(input("enter the value of b: "))

c = sum(a, b)
d = diff(a,b)
p = mul(a, b)
q = div(a,b)
h = mod(a,b)

print(c)
print(d)
print(q)
print(p)
print(h)
